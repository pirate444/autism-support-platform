  export type Language = 'en' | 'ar' | 'fr' | 'es';
  
  export const getTranslation = (key: string, language: Language = 'en', params?: Record<string, string | number>): string => {
    const translation = translations[language]?.[key] || translations.en[key] || key;
    
    if (params) {
      return Object.entries(params).reduce((str, [param, value]) => {
        return str.replace(new RegExp(`{${param}}`, 'g'), String(value));
      }, translation);
    }
    
    return translation;
  }; 